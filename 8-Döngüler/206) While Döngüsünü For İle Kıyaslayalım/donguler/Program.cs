namespace donguler;
class Program
{
    static void Main(string[] args)
    {
        #region while Döngüsü - for İle Kıyaslama
        
        for (int i = 0; i < 10; i++)
        {
            System.Console.WriteLine("Musa");
        }
        System.Console.WriteLine("**********************************");
        int _i = 0;
        while (_i < 10)
        {
            System.Console.WriteLine("Musa");
            _i++;
        }
        #endregion
    }
}
